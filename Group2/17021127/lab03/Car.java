public class Car {
    String model;
    String color;
    int vantoc;
    int slot;

    public void setModel(String model) {
        this.model = model;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public void setVantoc(int vantoc) {
        this.vantoc = vantoc;
    }

    public void setSlot(int slot) {
        this.slot = slot;
    }

    public String getModel() {
        return model;
    }

    public String getColor() {
        return color;
    }

    public int getVantoc() {
        return vantoc;
    }

    public int getSlot() {
        return slot;
    }
    
}
