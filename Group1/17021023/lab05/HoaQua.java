
public class HoaQua {
      String ten;
     String nguongoc;
     public HoaQua(String ten, String nguongoc) {
        this.ten = ten;
        this.nguongoc = nguongoc;
    }
     
     public void setTen(String ten) {
        this.ten = ten;
    }
     public String getTen() {
        return this.ten;
    }
     
     public void setNguonGoc(String nguongoc) {
        this.nguongoc = nguongoc;
    }
     public String getNguonGoc() {
        return nguongoc;
    }
}
