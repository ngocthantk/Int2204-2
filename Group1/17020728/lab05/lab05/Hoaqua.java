package lab05;

public class Hoaqua {

	private int gia;
	private String xuatxu;
	private String ngaynhap;
	private String soluong;

	public int getGia() {
		return gia;
	}

	public String getXuatxu() {
		return xuatxu;
	}

	public String getNgaynhap() {
		return ngaynhap;
	}

	public String getSoluong() {
		return soluong;
	}

	public void setHoaqua(int gia, String xuatxu, String ngaynhap, String soluong) {
		this.gia = gia;
		this.ngaynhap = ngaynhap;
		this.xuatxu = xuatxu;
		this.soluong = soluong;
	}
}
